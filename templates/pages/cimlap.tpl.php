<h3>A Papagájvilág Alapítvány véleménye és filozófiája a papagájtartásról:</h3>
<div class="field-item even" property="content:encoded"><p>&nbsp;</p>
<p>A Papagájvilág Alapítvány a papagájtenyésztés és tartás színvonalának fejlesztésével, valamint az illegális papagájkereskedelem elleni küzdelemmel foglalkozik a tájékoztatás és a képzés eszközeivel.</p>
<p>Honlapunkon igyekszünk mind a papagájok iránt érdeklődők, mind pedig a tenyésztők illetve tartók számára hasznos információkat szolgáltatni.</p>
<p>Munkánk vezérfonala az alábbi pontokban foglalható össze:</p>
<ol><li class="rtejustify">A papagájok az eredeti élőhelyükön tudnak teljes életet élni.&nbsp; Az alapítvány kiemelt célja, hogy munkájával hozzájáruljon a nemzetközi fajvédelmi programok sikeréhez és az illegális papagáj-kereskedelem visszaszorításához.</li>
<li class="rtejustify">Az alapítvány a papagájok fogságban tartását ismeretterjesztési célból az állatkertekben és madárparkokban, míg fajmegőrzési programok keretében arra akkreditált tenyészetekben támogatja.</li>
<li class="rtejustify">Az Alapítvány tudomásul veszi, hogy a papagájok népszerű házi kedvencként tartott madarak. Az alapítvány mindent megtesz annak érdekében, hogy a házi kedvencként tartott egyedek tartási kultúrája növekedjen Magyarországon.</li>
<li class="rtejustify">Az Alapítvány összhangban az a) pontban írtakkal támogatja, hogy az egyedi azonosító és származási igazolás nélküli papagájok az Állam tulajdonába kerüljenek, ugyanakkor az emberhez szokott és sérült, beteg egyedek esetében szorgalmazza, hogy azokat arra alkalmas magánszemélyek is gondozásukba vehessék.</li>
</ol><p>&nbsp;</p>
