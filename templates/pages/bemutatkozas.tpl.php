<h2>Alapítványunk:</h2>

<div class="field-item even" property="content:encoded"><p>A Papagájvilág Alapítvány célja a papagájalakúak (Psittaciformes) rendjébe tartozó madárfajok megóvásának elősegítése. Az Alapítvány kiemelten fontos célja a papagájok fajgazdagságának fenntartása a konzervációbiológia és a tájékoztatás eszközeivel, valamint a nemzetközi illegális állatkereskedelem visszaszorítása határokon átnyúló szakmai kapcsolatrendszer segítségével.</p>
<p><strong>Az alapítvány a céljait az alábbi konkrét tevékenységekkel kívánja elérni:</strong></p>
<ul><li>A felelősségteljes papagájtartás magyarországi meghonosításával.</li>
<li>A papagájokat bemutató és népszerűsítő tenyészetek szakmai támogatásával.</li>
<li>Állatorvosok és állatorvostan-hallgatók számára történő továbbképzések szervezésében való részvétellel.</li>
<li>Nemzetközileg elismert állatorvosokat, biológusokat, papagájkutatókat és viselkedés-specialistákat felvonultató konferenciák szervezésével.</li>
<li>Fajmegőrzési tenyészprogramokban való szakmai részvétellel.</li>
<li>A papagájfajok megismerését elősegítő programok szervezésével és támogatásával.</li>
<li>Nemzetközi, terepi természetvédelmi programokhoz történő kapcsolódással.</li>
<li>Rászoruló papagájok mentésével, elhelyezésével, egészségügyi ellátásával és rehabilitálásával.</li>
</ul><p>&nbsp;</p>
